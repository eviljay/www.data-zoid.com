<SPAN CLASS="comment">My life story, such as it is.</SPAN><BR>
Before you start reading this, I feel the need to point out to you that it's not really all that exciting. It's really just something I thought I'd add to take up some space on this website and make it look a 'lil more professional. ;-)<br>
<BR>

I was born on the 25th of April, 1981 at Cooma Base Hospital, in Cooma (of all places) in the mountainous south of New South Wales. (Redundancy, anyone?) My mother was overjoyed. My father thought I looked like a skinned rabbit. I was indifferent, and slept a lot.<br>
<BR>

I was named after the nurse who helped my mother after my birth. Her name was Nurse Russell, a fact I'll volunteer for those of you who can't put two and two together. I know there are a lot of you. I don't discriminate.<br>
<BR>

I believe I lived with my family in Cooma for at least eighteen months after my birth. I don't know for certain, my own memories of that time are foggy at best. I could have lived with a pack of wolves, for all I know. (I'd probably have been better off.)<br><BR>

After the brief Cooma jaunt, we moved to Stuart Mill in Victoria. There isn't much to Stuart Mill, and I don't anticipate that there ever will be. Imagine a highway in the middle of a lightly hilly area, with about four farmhouses positioned haphazardly about it and all of the traffic heading straight through it without ever stopping, and you've got a reasonable picture of S'Mill in it's heyday. My parents made and sold garden gnomes in Stuart Mill, and apparently did a roaring trade. I can't speak for certainty, I wasn't very interested in financial ongoings at that point in my life. I think I'd rather have eaten dirt. In fact, I think I did.<br><BR>

I do recall having painted several garden gnomes in my childhood, though. I have vivid memories of painting a small concrete horse bright pink. (By the way, the term "garden gnomes" applies to basically anything cast in concrete for the purposes of garden decoration, not just elven men with beanies and apparent mischievious nocturnal habits.) I think my grandmother still has the horse.<br><BR>

My father was a volunteer firefighter in the Victorian Bush Fire Brigade, which lead to my parents ultimate decision to pack up and bugger off northwards. In 1985, my father was called up to fight in the (now famous) Ash Wednesday fires, in rural Victoria, not far from where we lived. We left the following year.<br><BR>

We eventually moved to Lightning Ridge, in north-western New South Wales. Not my favourite area, but of course I didn't realize this at five years of age.<br><BR>

We visited the place once before permanently moving. I remember the trip up there, most of which was at night. I think we got in at about 5AM, because I can distinctly remember my father griping about the lack of places open to get food and/or fuel for the car. (Which was a Dodge ute, should you be interested.) We lived in a caravan on Nobbies Hill for a year to begin with. I remember there was a partially buried black Bedford bus outside, which I enjoyed "driving", even though the rear part of it was submerged several feet into a dried up silt trap. I can also remember my grandparents visiting us once whilst we lived in said caravan, and I remember being sick for the length of their visit, too. That visit was the last time I saw my grandfather alive.<br><BR>

Eventually, we moved into a slightly more established home. It was a fairly large sandstone and concrete building, with three watertanks to one side, and a mosaic of cemented white and green glass wine bottles in the living room wall. It was a fairly nice place, but lacked a second entrance, which scared my mother in case of fire. We lived there for about a year again, until it's other flaw started to compete with Lightning Ridge's climate - No airconditioning.<br><BR>

We spent several weeks bludging summer accomodation off my grandmother, who had moved to Lightning Ridge following my grandfather's death in 1987. She lived in a large caravan in a local caravan park. It was small and clumsy for four people, but it was air conditioned.<br><BR>

Everyone got sick of the situation. Quickly.<br><BR>

Dad bought a house. A real house. A house with four walls, two doors, and an air conditioner. I remember the week we moved in, too. We had practically no furniture (our previous home was fully furnished, which it remained when we left), and I slept on pillows on my "bedroom" floor. My bedroom was originally the dining room, off to one side of the kitchen. For the first two years that we lived there I had no bedroom wall, just an archway leading straight kitchenwards. Eventually my father made a wall. With a door.<br><BR>

We lived there until 1995. Renovated several times, it went from a one bedroom house to a four bedroom house, which was odd, because there were never more than three of us, and we rarely had guests. We still own the house.<br><BR>

We moved again in '95, to a large house on the Barwon River, near Collarenebri. My father had always wanted a house on the river, so he was overjoyed with the opportunity. I wasn't.<br><BR>

In early 1999, I moved back to Lightning Ridge, and back into the house we'd lived in before. It'd been more or less ransacked by the tenants who rented it in our absence, which lead me to a two-year long renovation, which - oddly - I enjoyed immensely.<br>
Around April '99 I became an active presence online, and met a whole bunch of people all over the world, all but a few of which have since faded into obscurity. I still keep in touch with several.<br><BR>

I've since moved to Sydney in an attempt to build myself a better life in an environment where job opportunities aren't so scarce and even holding a menial job can have a sense of purpose to it. I've only been here a few weeks now, but so far things are going fairly well. Stay tuned for probably no updates. <BR>
<BR>
<SPAN CLASS="comment">Update:</SPAN>...Well, the Sydney thing didn't pan out too well in the end. Basically, everything that could go wrong...did. Centrelink decided they didn't want to give me any money, I applied at forty nine places of employment and got nary a callback, and had no source of income whatsoever for the ten weeks I was in Sydney. I also broke up with my girlfriend and came close to murdering various members of my family.<BR>
<BR>
Oh, and I saw Bardot live in concert. Pity me. I deserve it.<BR>
<BR>
In retrospect, it hasn't dissuaded me from trying again some time, I just need a 'lil more preparation. Melbourne's looking good. :)<BR>
