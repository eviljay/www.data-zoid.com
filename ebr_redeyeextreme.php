<table width="100%" cellpadding="1" cellspacing="1"><tr><td class="menuhead" align="center">Appearance, Aroma, argh?</td></tr></table>
<BR>
<DIV style="text-align: center;"><IMG SRC="images/ebr/ebr_redeyeExtreme.jpg" WIDTH="240" HEIGHT="320" ALT="Beverage Bottle, glass"><BR>
<SPAN CLASS="tiny">It does indeed feature a large red eye.</SPAN></DIV>
<BR>
<b>To the eye:</b><BR>
It looks like cheap champagne. It's tinged slightly yellow, which is actually surprising as in traditional energy drink style it comes housed in a green glass bottle to mask its true appearance.<BR>
The beverage is "lightly carbonated", which manifests itself as several squillion tiny bubbles clinging to the inside of the glass, as shown in the next photograph. It should be noted that the stuff loses its carbonation at a rate of knots, and leaving it in the glass for any more than a few minutes renders it flat as a tack.<BR>
<BR>
<DIV style="text-align: center;"><IMG SRC="images/ebr/ebr_redeyeExtreme_closeup.jpg" WIDTH="320" HEIGHT="240" ALT="Beverage closeup"><BR>
<SPAN CLASS="tiny">Billions of small bubbles clinging to the glass.</SPAN></DIV>
<BR>
<b>To the nose:</b><BR>
It smells like...those powdery candy bananas everyone used to eat as a child. The ones that are made from the same substance as musk sticks, but taste of banana instead. It also has a mild aroma of some undefined chemical, although it's an energy drink, so this is to be expected. Removing the cap from the bottle produces a fairly large rush of carbonation to the surface of the drink, probably during which the majority of the carbon dioxide escapes, thus beginning the end of the bubbles as mentioned above.<BR>
<BR>
<b>To the mouth:</b><BR>
It actually tastes like candy bananas. It's quite pleasant, and not overpowering with any of the usual energy drink flavours (i.e. vegetables, medicine, wee). It loses its flavour quite speedily as the product loses its chill, however, so it's best to either drink it in its entirety while it's still as cold as possible, or refrigerate it and ration it into speedily consumed glasses. A lot of effort, perhaps, but a pleasant tasting energy drink is a rarity at best.<BR>
<BR>
<b>And a little while after:</b><BR>
The aftertaste is quite mild. It's basically just a residue of whatever produced the slight chemical odour that was present in the sniffing stage, and it fades quite quickly. The only unpleasance is a slight sugary coating it leaves on the lips and teeth.<BR>
<BR>
<b>Overall:</b><BR>
Without a doubt the most pleasant energy drink available. Tastes basically like an odd-flavoured soft drink. As for its energy inducing powers, I've not felt anything out of the ordinary. Although having sampled it, I don't feel I need to retch. Perhaps that <i>is</i> out of the ordinary after all.<BR>
<BR>
<b>One word summary*:</b><BR>
Bananas.<BR>
