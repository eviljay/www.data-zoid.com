<b>Biscuits:</b><BR>
This isn't a beverage. But it is exotic. And this is a review of it. Two out of three ain't bad.<BR>
<BR>
Everyone loves Tim Tams. So far, through numerous different flavours, neither Tim or Tam have managed to do anything wrong. You can understand my hesitance when I spotted "Tim Tam Dangerous Liaisons: Chilli Choc Fling" on my supermarket shelves.<BR>
<BR>
Actually, I lie. I didn't hesitate at all to stuff a pack into my shopping crate. I also purchased the Black Forest flavoured 'Tams, which I'll review later on if they prove themselves exotic enough.<BR>
<BR>
<DIV style="text-align: center;"><IMG SRC="images/ebr/ebr_timtams_chili.jpg" WIDTH="320" HEIGHT="240" ALT="Beverage Bottle, glass"><BR>
<SPAN CLASS="tiny">I hate to be the one to point this out, but..<BR>
..it's a bit phallic, right?</SPAN></DIV>
<BR>
<b>To the eye:</b><BR>
They look like regular dark chocolate Tim Tams. I was expecting the creme filling to be bright red or something, but alas it just looks like dark choccie.<BR>
<BR>
<DIV style="text-align: center;"><IMG SRC="images/ebr/ebr_timtams_closeup1.jpg" WIDTH="320" HEIGHT="240" ALT="Beverage closeup"><BR>
<SPAN CLASS="tiny">Little biscuits all lined up in a row.</SPAN></DIV>
<BR>
<b>To the nose:</b><BR>
It smells like really strong dark chocolate. Alas, nothing too exciting so far.<BR>
<BR>
<b>To the mouth:</b><BR>
I had some concerns. Particularly, that the <i>flavour</i> of the chilli would be present. I was unimpressed with the idea of a chocolate buscuit with undertones of capsicum. However, I was pleasantly surprised.<BR>
<BR>
The initial taste is of nothing but dark chocolate, just like a regular Dark Chocolate Tim Tam. By the time you've finished the biscuit, though, the chilli rears its ugly head and proceeds to burn the sweet buggery out of your mouth. Or not, really. It's mildly spicy. Actually, it's quite pleasant.<BR>
<BR>
<DIV style="text-align: center;"><IMG SRC="images/ebr/ebr_timtams_closeup.jpg" WIDTH="320" HEIGHT="240" ALT="Beverage closeup"><BR>
<SPAN CLASS="tiny">Decimated biscuit.</SPAN></DIV>
<BR>
I'm slightly concerned with what the temperature of a hot beverage would do to the chilli flavour, as everyone is aware that adding heat to chilli actually makes the spiciness somewhat hotter. I'm tempted to try a Tim Tam straw (bite the ends off and suck coffee through the biscuit), but I can't be arsed to make a cup of coffee.<BR>
<BR>
<b>And a little while after:</b><BR>
The chilli heat actually lasts quite a while. Even after having subjected my palate to a serving of <a href="ebr_redeyeplatinum.php">Red Eye Platinum</a>, I can still taste the zing.<BR>
<BR>
<b>Overall:</b><BR>
A bizarre concept that turns out to be quite pleasant in the end.<BR>
<BR>
<b>One word summary*:</b><BR>
Zing!<BR>
