<SPAN CLASS="tiny">Positronbob assists a breast self-examination.</SPAN></CENTER><BR>
<BR>
On the top level of the museum stood the beginnings of the "How To Make A Monster" display, which was to deal with animatronics and special effects. Sadly, it wasn't open yet. Regardless, I managed to milk a fairly nifty colonpipe.com wallpaper image from a photograph we asked a kind tourist to take for us. We didn't realise at the time that said tourist didn't speak a word of English. I was somewhat concerned when she started gibbering in some Asian language, and I hoped dearly that she did not think we were complimentary camera handing-out people, or something.<BR>
<BR>
<a href="photos/dz_rc_yz_pb_f/howtomakeamonster.jpg" TARGET="new">HOW TO MAKE A MONSTER wallpaper</a> (1024*768)<BR>
<BR>
I got my camera back, for the record.<BR>
<BR>
Moving away from the animatronics exhibit, we found....animatronics. In the form of a stuffed leopard mounted atop a papier-mache rock. Unassuming, you may think, until you realise (under close scrutiny) that the cat has a half-eaten sausage roll jammed up its bum.<BR>
<BR>
<CENTER><A HREF="photos/dz_rc_yz_pb_f/anal_pastry.jpg" TARGET="new"><IMG SRC="photos/dz_rc_yz_pb_f/anal_pastry_tn.jpg" WIDTH="320" HEIGHT="240" BORDER="0"></A><BR>
<SPAN CLASS="tiny">Sausage Rolls: To be consumed orally</SPAN></CENTER><BR>
