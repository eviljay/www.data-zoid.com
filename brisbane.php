<H2>PART ONE</H2></CENTER>
Here's a somewhat vague travelogue of my trip to Brisbane, and the three days during which I met Positronbob, Felicity, <a href="http://www.fullyramblomatic.com/" target="new">Yahtzee</a> and Rhubarb Celestial. Click on the pictures for bigger ones.<BR>
<BR>
<b>DAY ONE</B> (Sunday)<BR>
<BR>
Having determined where Positronbob and Felicity were staying, went and collected them. Drove to Kingston railway station, deposited car in car park. Took the train into the city, after bumming train fare from Positronbob. Spent most of the duration of the train trip discussing online activities on the <a href="http://www.the-underdogs.org/forum" target="new">HOTU Forum</a>, and the remainder of the time musing about how ridiculous it was to be discussing those things to begin with.<BR>
<BR>
Arrived in the city to discover that at some point between Kingston and Brisbane it had become fucking hot. Wandered out of South Bank station, where we were stopped - all three of us - in our tracks by the stultifying stupidity of this sign:<BR>
<BR>
<CENTER><A HREF="photos/dz_rc_yz_pb_f/blindpersons.jpg" TARGET="new"><IMG SRC="photos/dz_rc_yz_pb_f/blindpersons_tn.jpg" WIDTH="240" HEIGHT="320" BORDER="0"></A></CENTER><BR>
<BR>
Although on later reflection we realised the sign is probably there for the benefit of commuters passing on the road to the right. <BR>
<BR>
On further reflection on the above realisation, we again noticed that even if the sign is for motorists, there's still the issue of how the blind people know where to cross. <BR>
<BR>
It hurts my brain to think about it.<BR>
<BR>
We ate at a small cafe near the Energex Arbour. The Arbour is a massive winding footpath covered in with a hideous metal framework with bouganvilliea growing all over it. Eventually, I postulate, it will form an impenetrable forest of thorned and vicious plantlife, to be traversed only by warriors dressed in khaki and weilding machetes. As it stands, it's a poor attempt at even blocking out the sun.<BR>
<BR>
Felicity managed to rip the small cafe off, in that she ordered a custard muffin and two drinks and somehow got the muffin for around fifty cents. We were all fairly proud of this. Apparently custard is a rare commodity, as the serving she recieved would not have overflowed from a teaspoon.<BR>
<BR>
We waited under the Suncorp Piazza for Yahtzee and Rhubarb to show. We kind of realised that we knew nothing about their appearance apart from some really old online photos, which lead us to wonder whether the photos were even of them at all. Perhaps they just typed a random name into <a href="http://images.google.com/" target="new">Google Image Search</a> and used whatever photos popped up.<BR>
<BR>
To cut a long story, uh, less long, we found them. And then we stood about. Awkwardly. For quite a while.<BR>
<BR>
Then we ate. Again. Yahtzee ordered chips. We fed several of them to a hideously unattractive bird that was hovering about the table. Then her husband told us to stop. Ba-dum-KSH.<BR>
<BR>
Brisbane is a complete arse of a city when it comes to things to do. Basically, once you cross the border from New South Wales into Queensland, the passtime of "do things" mutates into the passtime of "do fuck all". People in Queensland spend 99% of their time doing nothing. The rest they spend sleeping. Having come to no conclusions whatsoever as to what to do to pass the day, we headed vaguely towards the city. I kind of recall having visited the museum a few years earlier, specifically due to said museum having a button on the wall that replicated the sound effects of a whale's bowel motions. We figured the museum would be a decent place to start.<BR>
<BR>
Brisbane Museum appears to have been organised by blind syphilitic chimpanzees. There's no system of order to it whatsoever. One second you're staring at a dinosaur's femur, the next you're examining a tandem-bicycle-powered-fire-engine and wondering where the connection was.<BR>
<BR>
<CENTER><A HREF="photos/dz_rc_yz_pb_f/doubletandem_firecart.jpg" TARGET="new"><IMG SRC="photos/dz_rc_yz_pb_f/doubletandem_firecart_tn.jpg" WIDTH="320" HEIGHT="240" BORDER="0"></A><BR>
<SPAN CLASS="tiny">Firecart. Neener, neener.</SPAN></CENTER><BR>
<BR>
Alongside the fire truck stood an array of cardboard cutouts.<BR>
<BR>
<CENTER><A HREF="photos/dz_rc_yz_pb_f/yahtzee_cutout.jpg" TARGET="new"><IMG SRC="photos/dz_rc_yz_pb_f/yahtzee_cutout_tn.jpg" WIDTH="320" HEIGHT="240" BORDER="0"></A><BR>
