But wait, there's more! (More animatronics. Not more anal pastry.)<BR>
<BR>
The next room featured a warning sign in a large font, reading something to the effect of "PARENTS: THE NEXT EXHIBIT FEATURES A <B>VERY REALISTIC LIZARD</B> WHICH MAY FRIGHTEN CHILDREN". And behold:<BR>
<BR>
<CENTER><A HREF="photos/dz_rc_yz_pb_f/warning_realistic_lizard.jpg" TARGET="new"><IMG SRC="photos/dz_rc_yz_pb_f/warning_realistic_lizard_tn.jpg" WIDTH="320" HEIGHT="240" BORDER="0"></A><BR>
<SPAN CLASS="tiny">Murrr. Shudder. Thud. Crunch.</SPAN></CENTER><BR>
<BR>
Somewhat disturbingly, it moved right as my camera flashed. Yet more disturbing, though, is the rest of the exhibit. Surrounding the giant lizard is a large papier-mache dinosaur corpse with several Tasmanian devils feeding on it. One of them tugs back and forth on a chunk of rubber intestine, while another appears to perform oral sex on the dead reptile.<BR>
<BR>
The rest of the museum paled in comparison. Several rooms containing spiders in jars. Several rooms containing randomly placed fibreglass animal replicas scaling the walls. If nothing else, a new theme song emerged:<BR>
