The ColonPipe
</TD>
</TR>
</TABLE>
<BR>
This is single handedly the most destructive force in chat. The Colon Pipe. Not, as many people think, a body part. This is a face made from the conjoining of a colon ":" with a pipe-symbol "|" to create a powerful vision indeed.<br>
<br>
The <img src="yahoo_poker.gif">.<br>
<br>
Many people take the <img src="yahoo_poker.gif"> to mean intense dislike. Sometimes even hate. They couldn't be further from the truth. <img src="yahoo_poker.gif"> represents frustration, annoyance. Sometimes just plain boredom. There are many ways to use a <img src="yahoo_poker.gif">. The first use was to place it at the end of a sentence in order to evoke a sense of sarcasm.
Examples.<br>
<br>
"Oh yes. I can see where this is headed. <img src="yahoo_poker.gif"> "<br>
"I do like it. Honest. <img src="yahoo_poker.gif"> "<br>
"Aren't we lively today? <img src="yahoo_poker.gif"> "<br>
<br>
However, overuse has more or less killed this use off. Instead, a different sort grew in it's place. The infamous "<img src="yahoo_poker.gif"> @ X". Let's break it down into it's component parts.<BR>
"<img src="yahoo_poker.gif">"<br>
The ColonPipe, still used to display annoyance or frustration at something. If this was a physical sound, it would most likely be "Mmph."<br>
"@".<br>
At. Never written as "At", but always as "@".<br>
"X".<br>
The X can be anything. Absolutely anything at all. It matters not whether X is a good or bad thing.<br>
<br>
To put it into perspective, the <img src="yahoo_poker.gif"> @ X is a simple and expressive way about showing how annoyed you are about something. Exclamation marks can be used to emphasis this somewhat. The exclamation marks in question must always follow the <img src="yahoo_poker.gif">, rather than being at the end of X.
Examples.<br>
<br>
<img src="yahoo_poker.gif"> @ Sting.<br>
<img src="yahoo_poker.gif"> @ Morons.<br>
<img src="yahoo_poker.gif"> !! @ Yahoo.<br>
<img src="yahoo_poker.gif"> !!!!! @ Lag.<br>
<br>
On occasions, the "@ X" is overkill, so can be discarded.<br>
<br>
Datazoid - "I saw another picture of Sting<link> yesterday. He was covered in jelly this time."<br>
Gremmi - "<img src="yahoo_poker.gif"> !!!"<br>
<br>
As from the above example, there'd be no point writing out the entire "<img src="yahoo_poker.gif"> @ Sting<link> covered in jelly". <br>
<br>
An additional way of emphasising the intensity of the <img src="yahoo_poker.gif"> is to perform arithmetic using the colon pipe. The most basic example of this is as follows:<br>
<br>
<img src="yahoo_poker.gif"> <img src="yahoo_poker.gif"> <img src="yahoo_poker.gif"> & <img src="yahoo_poker.gif">.<br>
<br>
More than four <img src="yahoo_poker.gif">s in one sentence is generally considered overkill.<br>
<br>
It's optional to add the "@ X" to colon pipe arithmetic, but as in the use of exclamation marks, the intended target of the <img src="yahoo_poker.gif"> is usually fairly clear.
Here are some other examples of mathematical <img src="yahoo_poker.gif">ing:<br>
<br>
<img src="yahoo_poker.gif"> x 10^7.<br>
<img src="yahoo_poker.gif"> ad infinitum.<br>
<br>
Overkilling the <img src="yahoo_poker.gif"> is a heinous crime, only ever exhibited by the newbiest of newbies<link>.<br>
The <img src="yahoo_poker.gif"> is probably the most important of the smileys<link>. It's the most used, at least.
<p align=right>- Gremmi, "<i>A GreyShirt, a Gremlin, A Zoid and A Chatroom</i>".</p>

