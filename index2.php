The <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1496" target="new">cabins on the boat</a> are pleasant, if tiny. Lots of mirrors. <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1497" target="new">Bathroom's</a> quite roomy, and has a <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1498" target="new">shower</a> that's fun to use on rough seas. Got woken up twice on the trip over, once with my head being crammed into the wall by a peaking wave, the other mid-peak, leaving me feeling weightless for the following flop. <br />
<br />
Arrived in Tasmania. Drove to the north-west corner. Tasmanians can't drive, and need to be formally introduced to white painted lines, which they seem to think are guidelines to drive down, not to divide the road.<br />
<br />
It <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1501" target="new">snowed</a> for the first time in two decades. We made <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1502" target="new">snowmen</a>. <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1504" target="new">Lots</a> of snowmen. Here I am <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1508" target="new">catching some snow</a>.<br />
<br />
Visited a place called Stanley, and it's odd <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1513" target="new">outcropping of rock</a>, called "The Nut". There's usually a <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1515" target="new">chairlift</a> in operation to haul your lazy arse to the top of The Nut, but as luck would have it, it doesn't service in winter. So we <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1514" target="new">scaled</a> The Nut instead. The tracks ascend at about a 45-degree angle. Mm, calf-burny. <br />
The top of the nut is a giant disc of rock with trees and stuff on it. You walk around a 2km track, circumnavigate it, and walk down the other side. <br />
<br />
A few days later, visited Launceston and Hobart. Launceston's a great place if you're not a vehicle's wing mirror. Clearly the streets were designed for about half a car, and have been marked for three lanes. Three people walking abreast would bump shoulders down these streets. <br />
<br />
Drove to Hobart via Perth (Tasmania) and a small town called Campbell Town (not to be confused with Campbelltown, Sydney). Campbell Town has a kind of "Walk Of Infame" on its main street, where convicts are assigned <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1516" target="new">bricks</a> with their name and brief life story on.<br />
<br />
Continued to Hobart. Looked at the city from the lookout. Scouted cool looking houses. I think these are the <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1519" target="new">tallest buildings</a> in Hobart. It's not a tall city.<br />
<br />
The power of Christ compels you! It's possibly not the <a href="http://rafters.data-zoid.com/album_pic.php?pic_id=1520" target="new">steps from The Exorcist</a>.<br />
<br />
But wait, there's more!<br />
