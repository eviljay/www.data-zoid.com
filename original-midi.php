<BR>
Various original musical..composition..things. If you can even call them that. In no particular order.<BR>
<BR>
<hr />
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/advancedsixfour.mid">Advanced 6/4</A> - It's in 6/4 time. Obviously. It started out as a kind of Australian folk tune with a rock bent to it, then it turned crappy. It's repetitive as hell, but some people like that.<BR>
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/arabiandays.mid">Arabian Days</A> - Arabian-ish sort of tune. Loosely based on the introductory song from Aladdin.<BR>
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/beside_the_sky.mid">Beside The Sky</A> - This was an experiment using a chord structure dug out of a really old book of piano lessons. It was meant to sound kind of Ben Foldsy, but turned out to be speedy crap that'd fit in with a Japanese cartoon or something.<BR>
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/bloodiedspiderwebs.mid">Bloodied Spiderwebs</A> - Written for Miek. He said "Russ, write something piano-ey! One of those pieces that's actually really slow, but the techno drum beat makes it sound speedy!". So I did. And he <a href="http://fraxyl.data-zoid.com/fraxyl%20and%20russ%20-%20bloodied%20spiderwebs.mp3">remixed it</a>.<BR>
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/carcinogenic_monkey_grease.mid">Carcinogenic Monkey Grease</A> - Obviously a tune composed during my "just use random words for a title" phase. This is vaguely inspired by Elliott Smith, particularly the lil rise before the chorus and the general tempo. It has nothing to do with monkeys.<BR>
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/dirty_jazz.mid">Dirty Jazz</A> - Just jazz. Just piano. Nothing spectacular.<BR>
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/done.mid">Done</A> - This was actually written with lyrics, but I can't remember what they are. This is probably a good thing, as my lyrics stink.<BR>
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/evolution.mid">Evolution</A> - This one also had lyrics. Just messin' with bass, really. Dunno whether this is any good or not, sounded pretty crappy when I wrote it.<BR>
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/guitar4.mid">Guitar 4</A> - This one's almost alright. It's pretty intense, though. My MIDI synth has some difficulty churning it out. Repetitive, but potentially alright for harmonies or something. No doubt sounds a shitload better on actual instruments.<BR>
<BR>
<SPAN STYLE="color: #555555;">&nbsp;MIDI -</SPAN>&nbsp;<A HREF="http://midi.data-zoid.com/completedoriginalmidi/guitarjam.mid">Guitar Jam</A> - Just guitar.<BR>
<BR>
