<H2>PART THREE</H2></CENTER>
Here's a somewhat vague travelogue of my trip to Brisbane, and the three days during which I met Positronbob, Felicity, <a href="http://www.fullyramblomatic.com/" target="new">Yahtzee</a> and Rhubarb Celestial. Click on the pictures for bigger ones.<BR>
<BR>
<b>DAY TWO</B> (Tuesday. Where did Monday go? Fuck knows!)<BR>
<BR>
I spent most of the morning in Surfers Paradise. There are a lot of reasons I dislike Surfers Paradise. One of them is grammatical. Y'see, it's missing a possessive apostrophe. It's something to do with naming conventions, in that suburb titles should not contain punctuation marks. Pop quiz: What is the only location in the United States to have a posessive apostrophe in? If you give a fuck, the answer is at the end of this page.<BR>
<BR>
So. Surfer(')s Paradise. 9AM. NOTHING IS FUCKING OPEN. I guess the surfy culture doesn't wake up prior to, oh, 4PM. Anyhow.<BR>
<BR>
Got back from the Gold Coast around lunch time. Went to collect Positronbob and Felicity, with the plan being to go Christmas shopping at the Logan Hyperdome.<BR>
<BR>
<CENTER><A HREF="photos/dz_rc_yz_pb_f/frrresh.jpg" TARGET="new"><IMG SRC="photos/dz_rc_yz_pb_f/frrresh_tn.jpg" WIDTH="320" HEIGHT="240" BORDER="0"></A><BR>
<SPAN CLASS="tiny">It has three "r"s in! And it looks like the ABC shop won out.</SPAN></CENTER><BR>
<BR>
This evening we went bowling. Dan won the first round, I won the second. I can't remember who wound up with the scorecards, so you'll have to take my word for it. Truth be told, no one cares anyway.<BR>
<BR>
<b>DAY THREE</B> (Wednesday.)<BR>
<BR>
We basically started today with no clue whatsoever what we were doing. I drove around most of the morning trying to figure out where Pbob and Felicity were, due to missing one pissy little side street and thusly becoming lost.<BR>
<BR>
I found them, eventually. Called Rhubarb, arranged to meet in the city. Decided, in a rare moment of braveness, to drive into the city. The guy I was staying with suggested a spot for parking, and having glanced at a map I figured it was a fairly simple place to get to. So off we went. Found ample parking. Everything going well.<BR>
<BR>
Met Yahtzee and Rhubarb in the same location again, generally minced about looking confused and sounding unconversational. Somewhat photojournalistic article to ensue:<BR>
<BR>
<CENTER><A HREF="photos/dz_rc_yz_pb_f/wang_dynasty.jpg" TARGET="new"><IMG SRC="photos/dz_rc_yz_pb_f/wang_dynasty_tn.jpg" WIDTH="320" HEIGHT="240" BORDER="0"></A><BR>
<SPAN CLASS="tiny">The Wang Dynasty!</SPAN></CENTER><BR>
<BR>
My exaggerated pleas for someone to place their hand casually over the "cha" were not met with good graces. Sigh.<BR>
<BR>
While snacking at one of the small cafes along the Energex Arbour, we also happened upon a freak occurance, Santa Claus on his lunch break, snacking merrily on a parcel of hot chips. It's nice to know it's Queensland fried foods that keep Jolly ol' St. Nick's arteries so joyously blocked.<BR>
