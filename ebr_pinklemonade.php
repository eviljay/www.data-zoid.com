<BR>
<b>Pink Lemonade:</b><BR>
Schweppes released "Traditional Pink Lemonade" some time around 1994. This isn't the same stuff, unfortunately, but it counts as exotic and thus gets a review. I searched Google Image Search pretty frantically, hoping to find a photograph of the original bottles TPL was sold in, but I've failed miserably. If anyone can get their hands on an image of the original packaging, or better yet, an actual bottle, please let me know.<BR>
<BR>
<DIV style="text-align: center;"><IMG SRC="images/ebr/ebr_pinklemonade.jpg" WIDTH="240" HEIGHT="320" ALT="Beverage Bottle, glass"><BR>
<SPAN CLASS="tiny">It's pinker than the original version.</SPAN></DIV>
<BR>
<b>To the eye:</b><BR>
It tells no lies, indeed it is pink. It's also slightly milky.<BR>
<BR>
<DIV style="text-align: center;"><IMG SRC="images/ebr/ebr_pinklemonade_closeup.jpg" WIDTH="320" HEIGHT="240" ALT="Beverage closeup"><BR>
<SPAN CLASS="tiny">Although it's not really <i>this</i> pink.</SPAN></DIV>
<BR>
<b>To the nose:</b><BR>
It smells kind of sickly sweet, actually. The strawberry component smells more like strawberry yoghurt.<BR>
<BR>
<b>To the mouth:</b><BR>
Pink lemonade, precisely. It tastes like Schweppes Traditional Lemonade (i.e. basically Solo or Lift) with a hint of strawberry. The only downside to this is that strawberry's earned itself a reputation as being a dairy flavouring, like chocolate. You see strawberry ice cream and strawberry milk, but rarely strawberry soft drinks or jellies. So there's a slight sensation of dairyness to it. Still, palatable in the extreme.<BR>
<BR>
<b>And a little while after:</b><BR>
The strawberry flavour takes on a slightly plastic taste after a while. It's nothing unusual, though.<BR>
<BR>
<b>Overall:</b><BR>
Schweppes has definitely changed the recipe at some point, as this stuff does not taste like the original "Traditional Pink Lemonade" they manufactured about ten years ago. To wit, this stuff is called simply "Pink Lemonade"; there's no tradition about it. The original was a much paler beverage, also, and tasted less manufactured.<BR>
<BR>
<b>One word summary*:</b><BR>
Dairyness.<BR>
