<BR>
<b>To the eye:</b><BR>
It's, um, turquoise. I was not expecting turquoise. I've had this stuff before, and all I can recall is that it tasted pretty horrible, so seeing it outside the can was something of a surprise. Sprite Recharge bottles are coated, head-to-toe, in opaque labels, so even the glass containers provide no hint as to the blue-ness hiding inside. It remains carbonated for a great length of time, also.<BR>
<BR>
Also worth noting is that the Sprite Recharge cans are exorbitantly lengthy, containing an extra inch of aquamarine goodness. I postulate that a further inch is required to provide the same amount of fluid as in a regular shaped can.<BR>
<BR>
<DIV style="text-align: center;"><IMG SRC="images/ebr/ebr_spriterecharge_closeup.jpg" WIDTH="320" HEIGHT="240" ALT="Beverage closeup"><BR>
<SPAN CLASS="tiny">It's freakin' blue. </SPAN></DIV>
<BR>
<b>To the nose:</b><BR>
It does not have a strong odour. It vaguely stinks of medicine and vitamins, which is odd as it does not seem to contain the usual myriad vitamins these drinks boast.<BR>
<BR>
<b>To the mouth:</b><BR>
It tastes quite medicinal. There's a good portion of earthy vitamin flavours hiding behind it, and there's a zing of something slightly anaesthetic, possibly mint.<BR>
<BR>
<b>And a little while after:</b><BR>
The anaesthetic taste/sensation continues, leaving an aftertaste of nothing but medication. I have a suspicion someone switched the vats of genuine Sprite Recharge with Listerine while the Coca Cola folk weren't looking, because quite frankly, it tastes like mouthwash.<BR>
<BR>
<b>Overall:</b><BR>
It's blue, tastes like mouthwash and remains fizzy for great lengths of time. Other than that, it's pretty morbid stuff.<BR>
<BR>
<b>One word summary*:</b><BR>
Turquoise.<BR>
